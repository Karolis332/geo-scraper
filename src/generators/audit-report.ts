function detectLanguage(crawlResult: SiteCrawlResult): string {
  const langCounts: Record<string, number> = {};
  for (const page of crawlResult.pages) {
    const lang = page.meta.language?.split('-')[0]?.toLowerCase();
    if (lang) langCounts[lang] = (langCounts[lang] || 0) + 1;
  }
  let topLang = 'en';
  let topCount = 0;
  for (const [lang, count] of Object.entries(langCounts)) {
    if (count > topCount) { topLang = lang; topCount = count; }
  }
  // Fallback for sites that omit lang attribute in HTML.
  if (topCount === 0 && crawlResult.domain.toLowerCase().endsWith('.lt')) {
    return 'lt';
  }
  return topLang;
}

